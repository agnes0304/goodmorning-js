// calculator
const calculator = {
    add: function(a,b){
        console.log(a+b);
    },
    minus: function(a,b){
        console.log(a-b);
    },
    mul: function(a,b){
        console.log(a*b);
    },
    div: function(a,b){
        console.log(a/b);
    },
    power: function(a,b){
        console.log(a**b);
    },
};

calculator.add(100,50);
calculator.minus(100,50);
calculator.mul(100,50);
calculator.div(100,50);
calculator.power(100,2);
